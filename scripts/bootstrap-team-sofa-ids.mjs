function pickBestMatch(results, query) {
  const rows = Array.isArray(results) ? results : [];
  const q = query.toLowerCase();
  const scored = rows
    .map((row) => {
      const t = row?.entity && typeof row.entity === "object" ? row.entity : row;
      if (!t || typeof t.id !== "number") return null;
      const name = String(t.name ?? "").toLowerCase();
      let score = typeof row.score === "number" ? row.score / 1_000_000 : 0;
      if (name === q) score += 100;
      else if (name.includes(q) || q.includes(name)) score += 50;
      if (t.national === true) score += 30;
      if (t.gender === "M") score += 20;
      if (t.gender === "F") score -= 50;
      if (name.includes(" u21") || name.includes(" u23") || name.includes(" u20")) score -= 40;
      return { t, score };
    })
    .filter(Boolean)
    .sort((a, b) => b.score - a.score);
  return scored[0]?.t ?? null;
}

async function searchTeam(query) {
  const path = `/api/sofascore/v1/search/teams?q=${encodeURIComponent(query)}`;
  const res = await fetch(`https://${HOST}${path}`, { headers: hdr });
  if (!res.ok) return null;
  const json = await res.json();
  return pickBestMatch(json, query);
}

const map = {};
const missing = [];

for (const abbrev of TEAM_LOGO_ABBREVIATIONS) {
  const query = SEARCH_NAMES[abbrev];
  if (!query) {
    missing.push(abbrev);
    continue;
  }
  await new Promise((r) => setTimeout(r, 450));
  const match = await searchTeam(query);
  if (match?.id) {
    map[abbrev] = match.id;
    console.log(`${abbrev}\t${match.id}\t${match.name}`);
  } else {
    missing.push(abbrev);
    console.warn(`MISSING\t${abbrev}\t${query}`);
  }
}

const out = `/**
 * SofaScore numeric team IDs for WC 2026 nations.
 * Generated by scripts/bootstrap-team-sofa-ids.mjs — re-run to refresh.
 */
export const TEAM_SOFA_IDS: Record<string, number> = ${JSON.stringify(map, null, 2)};

export function resolveSofaTeamId(abbrev: string): number | undefined {
  return TEAM_SOFA_IDS[abbrev.toUpperCase()];
}
`;

writeFileSync("src/data/teamSofaIds.ts", out);
console.log(`\nWrote ${Object.keys(map).length} IDs to src/data/teamSofaIds.ts`);
if (missing.length) {
  console.warn("Missing:", missing.join(", "));
  process.exit(1);
}
